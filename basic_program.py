def up_low(s):
    u=l=0
    for i in list(s):
        if i. isupper():
            u=u+1
        if i. islower():
            l=l+1
        else:
            pass
    print('no. of upper case characters:', u)
    print('no. of lower case characters:', l)
        
        
up_low('Hello Mr. Rogers, how are you this fine Tuesday?')
        